    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        SettingController controller = new SettingController();
        User userInfo = new User();
        userInfo.setId(user.getId());
        userInfo.setFullname(fullname.getText());
        userInfo.setEmail(email.getText());
        userInfo.setPhone(phone.getText());
        userInfo.setAddress(address.getText());
        userInfo.setGender((String) selectGender.getSelectedItem());
        userInfo.setPassword(password.getText());
       

        boolean success = controller.updateUserInfo(userInfo);

        if (success) {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
        }
        loadUserData();
    }//GEN-LAST:event_updateActionPerformed


