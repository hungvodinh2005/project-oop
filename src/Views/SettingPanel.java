public void loadUserData() {
    User userInfo=new SettingController().getUserInfo(user.getId());
    username.setText(userInfo.getUsername());
    password.setText(userInfo.getPassword());
    fullname.setText(userInfo.getFullname());
    email.setText(userInfo.getEmail());
    phone.setText(userInfo.getPhone());
    address.setText(userInfo.getAddress());

    
    if (user.getGender() != null) {
        selectGender.setSelectedItem(userInfo.getGender());
    } else {
        selectGender.setSelectedIndex(0);
    }

    
   
}
public void selectData(){
    selectGender.removeAllItems();
    selectGender.addItem("Nam");
    selectGender.addItem("Nữ");
}

