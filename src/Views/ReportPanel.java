    private void showReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showReportActionPerformed
          try {
        
        String month = selectMonthReport.getSelectedItem().toString();
        String year = selectYearReport.getSelectedItem().toString();
        String monthYear = year + "-" + month; // v√≠ d·ª•: "2025-10"

       
        ReportController rc = new ReportController();
        int userId = user.getId();

        
        Map<String, Double> summary = rc.getMonthlySummary(userId, monthYear);
        double income = summary.getOrDefault("income", 0.0);
        double expense = summary.getOrDefault("expense", 0.0);
        double balance = summary.getOrDefault("balance", 0.0);

       
        totalIncome.setText(String.format("%,.0f", income));      
        totalExpense.setText(String.format("%,.0f", expense));
        subIncomeExpense.setText(String.format("%,.0f", balance)); 
      

        
        int vuotMuc = rc.getExceededCount(userId, monthYear);
        overExpense.setText(String.valueOf(vuotMuc));

        
        Map<String, Double> expenseByCat = rc.getExpenseByCategory(userId, monthYear);

       
        ChartCategoryMonth.removeAll();
        ChartExpenseMonth.removeAll();

        if (!expenseByCat.isEmpty()) {
            
            DefaultPieDataset pieDataset = new DefaultPieDataset();
            for (Map.Entry<String, Double> e : expenseByCat.entrySet()) {
                pieDataset.setValue(new CategoryController().getCategoryById(e.getKey()).getName(), e.getValue());
            }

            JFreeChart pieChart = ChartFactory.createPieChart(
                    "Chi ti√™u theo danh m·ª•c (" + monthYear + ")",
                    pieDataset,
                    true, true, false
            );

            ChartPanel pieChartPanel = new ChartPanel(pieChart);
            ChartCategoryMonth.setLayout(new java.awt.BorderLayout());
            ChartCategoryMonth.add(pieChartPanel, java.awt.BorderLayout.CENTER);

            // Bi·ªÉu ƒë·ªì c·ªôt: T·ªïng thu - chi
            DefaultCategoryDataset barDataset = new DefaultCategoryDataset();
            barDataset.addValue(income, "T·ªïng Thu", month);
            barDataset.addValue(expense, "T·ªïng Chi", month);

            JFreeChart barChart = ChartFactory.createBarChart(
                    "T·ªïng thu - chi th√°ng " + month + "/" + year,
                    "Lo·∫°i",
                    "S·ªë ti·ªÅn",
                    barDataset
            );

            ChartPanel barChartPanel = new ChartPanel(barChart);
            ChartExpenseMonth.setLayout(new java.awt.BorderLayout());
            ChartExpenseMonth.add(barChartPanel, java.awt.BorderLayout.CENTER);

        } else {
            // Kh√¥ng c√≥ d·ªØ li·ªáu
            ChartCategoryMonth.setLayout(new java.awt.BorderLayout());
            ChartCategoryMonth.add(new JLabel("Kh√¥ng c√≥ d·ªØ li·ªáu chi ti√™u th√°ng n√†y.", SwingConstants.CENTER),
                                   java.awt.BorderLayout.CENTER);
        }

        // üîπ C·∫≠p nh·∫≠t l·∫°i UI
        ChartCategoryMonth.revalidate();
        ChartCategoryMonth.repaint();
        ChartExpenseMonth.revalidate();
        ChartExpenseMonth.repaint();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "L·ªói khi t·∫°o b√°o c√°o: " + e.getMessage(),
                "L·ªói", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }


    }//GEN-LAST:event_showReportActionPerformed
private void setSelectMonthYear() {
    // 1Ô∏è‚É£ X√≥a d·ªØ li·ªáu c≈© (n·∫øu c√≥)
    selectMonthReport.removeAllItems();
    selectYearReport.removeAllItems();

    // 2Ô∏è‚É£ Th√™m danh s√°ch th√°ng (01‚Äì12)
    for (int i = 1; i <= 12; i++) {
        String month = String.format("%02d", i);
        selectMonthReport.addItem(month);
    }

    // 3Ô∏è‚É£ L·∫•y nƒÉm hi·ªán t·∫°i
    java.util.Calendar cal = java.util.Calendar.getInstance();
    int currentYear = cal.get(java.util.Calendar.YEAR);
    int currentMonth = cal.get(java.util.Calendar.MONTH) + 1; // (0-based)

    // 4Ô∏è‚É£ Th√™m danh s√°ch nƒÉm (v√≠ d·ª•: 2020 ‚Üí nƒÉm hi·ªán t·∫°i)
    for (int y = 2020; y <= currentYear; y++) {
        selectYearReport.addItem(String.valueOf(y));
    }

    // 5Ô∏è‚É£ ƒê·∫∑t m·∫∑c ƒë·ªãnh ch·ªçn th√°ng v√† nƒÉm hi·ªán t·∫°i
    selectMonthReport.setSelectedItem(String.format("%02d", currentMonth));
    selectYearReport.setSelectedItem(String.valueOf(currentYear));
}

